unsigned char* ConvertEncoding_OneByte_To_UTF8(unsigned char* readBuffer, unsigned char* writeBuffer, int readBufferSize, const unsigned char* EncodingTable_OneByte_To_UTF8)
{
    unsigned int utf8;

    for (; readBufferSize > 0; readBuffer++, readBufferSize--)
	{
		utf8 = *(unsigned int*)(EncodingTable_OneByte_To_UTF8 + 4 * *readBuffer);

		*(unsigned int*)writeBuffer = utf8 >> 8;

		writeBuffer += utf8 & 0xff;
	}

    return writeBuffer;
}

